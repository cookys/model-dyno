const getStoredLocale = (): Locale => {
  try {
    const stored = localStorage.getItem('dash.locale')
    if (stored === 'en' || stored === 'zh') return stored
  } catch {}
  return 'en'
}

const currentLocale = ref<Locale>(getStoredLocale())

export function useI18n() {
  const locale = computed({
    get: () => currentLocale.value,
    set: (val: Locale) => {
      currentLocale.value = val
      try {
        localStorage.setItem('dash.locale', val)
      } catch {}
    }
  })

  const t = (key: string): string => {
    const dict = STRINGS[locale.value] || STRINGS.en
    if (key in dict) {
      return (dict as any)[key]
    }
    const defaultDict = STRINGS.en
    if (key in defaultDict) {
      return (defaultDict as any)[key]
    }
    return key
  }

  return {
    locale,
    t
  }
}
